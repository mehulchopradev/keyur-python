n = int(input('Enter n: '))

# if - else selection statement
# 2 branches
# else is not compulsory

''' if n % 2:
   print('Odd')
else:
   print('Even') '''

'''
    if - else comprehension
    Pre requisite: Both the branches must have only 1 statement of execution
'''
print('Odd') if n % 2 else print('Even')
